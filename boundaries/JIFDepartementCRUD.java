    private void jButtonModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModifierActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().equals("id")) {
            jLabelMessage.setText("Veuillez selectionner un Departement");
        } else {
            int liAffecte = 0;

            try {
                DepartementDAO dao = new DepartementDAO(icnx);

                // Update
                Departement di = new Departement(Integer.valueOf(jTextField1.getText()), jTextField2.getText(), jTextField3.getText());
                liAffecte = dao.update(di);
                if (liAffecte == 1) {
                    icnx.commit();
                }
                jLabelMessage.setText("Modifié : " + Integer.toString(liAffecte));
                remplirJTable();
            } catch (SQLException e) {
                jLabelMessage.setText(e.getMessage());
            }
        }

    }//GEN-LAST:event_jButtonModifierActionPerformed

