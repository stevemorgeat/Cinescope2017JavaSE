    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().equals("id")) {
            jLabelMessage.setText("Veuillez sélectionner une ligne !");
        } else {
            try {
                DepartementDAO dao = new DepartementDAO(icnx);

                // Delete
                Departement dd = new Departement(Integer.valueOf(jTextField1.getText()), jTextField2.getText(), jTextField3.getText());
                int liAffecte = dao.delete(dd);
                icnx.commit();
                if (liAffecte == 1) {
                    icnx.commit();
                    remplirJTable();
                }
                jLabelMessage.setText("Supprimé : " + Integer.toString(liAffecte));

            } catch (SQLException e) {
                jLabelMessage.setText(e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed


